    private boolean validaCampos() {
        if (jTiddistrito.getText().equals("")) {
            return false;
        }
        if (jFcnpj.getText().length() != 14) {
            return false;
        }
        if (jTrazao.getText().equals("")) {
            return false;
        }
        return true;
    }


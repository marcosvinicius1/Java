    private void jBexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBexcluirActionPerformed
        try {
            // TODO add your handling code here:
            if (!jTidempresa.getText().equals("")) {
                if (JOptionPane.showConfirmDialog(null, "Deseja Excluir o Registro", "Atenção", JOptionPane.YES_NO_OPTION) == 0) {
                    new EmpresaDao().deleteEmpresa(Integer.valueOf(jTidempresa.getText()));
                    ativaNovoAlterarExcluir();
                    limpaView();
                    desativaView();
                    buscaEmpresa();
                    JOptionPane.showMessageDialog(this, "Registro Excluido", "Excluir", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Selecione um Registro", "Excluir", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao Excluir" + ex, "Excluir", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jBexcluirActionPerformed


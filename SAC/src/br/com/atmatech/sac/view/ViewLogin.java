    private NimRODLookAndFeel getTema() {
        try {
            carregaConfig();
            NimRODTheme nt = new NimRODTheme();
            DBConfigBeans db = new DBConfigBeans();
            nt.setPrimary1(new Color(db.getPrimary1r(), db.getPrimary1g(), db.getPrimary1b()));
            nt.setPrimary2(new Color(db.getPrimary2r(), db.getPrimary2g(), db.getPrimary2b()));//cor da selecao
            nt.setPrimary3(new Color(db.getPrimary3r(), db.getPrimary3g(), db.getPrimary3b()));//cor da linha de menu
            nt.setSecondary1(new Color(db.getSecondary1r(), db.getSecondary1g(), db.getSecondary1b()));//cortono dos botoes
            nt.setSecondary2(new Color(db.getSecondary2r(), db.getSecondary2g(), db.getSecondary2b()));//contorno do painel
            nt.setSecondary3(new Color(db.getSecondary3r(), db.getSecondary3g(), db.getSecondary3b()));//background do frame
            nt.setWhite(new Color(db.getWhiter(), db.getWhiteg(), db.getWhiteb()));//COR DO FUNDO EDIÇÃO
            nt.setBlack(new Color(db.getBlackr(), db.getBlackg(), db.getBlackb()));//COR FONTE
            nt.setMenuOpacity(db.getMenuopacity()); //opacidade do menu
            nt.setFrameOpacity(db.getFrameopacity());
            NimRODLookAndFeel NimRODLF = new NimRODLookAndFeel();
            NimRODLF.setCurrentTheme(nt);
            return NimRODLF;
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Ler Arquivo Config" + ex, "Tema", JOptionPane.ERROR_MESSAGE);
            return null;
        }
    }


    private void jBsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBsalvarActionPerformed
        // TODO add your handling code here:
        if (validaCampos()) {
            if (jTidusuario.getText().equals("")) {
                cadastraUsuario();
            } else {
                alteraUsuario();
            }
        } else {
            JOptionPane.showMessageDialog(this, "HÁ Campos Obrigatorios", "Salvar", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jBsalvarActionPerformed

    private void jBalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBalterarActionPerformed
        // TODO add your handling code here:
        if (!jTidusuario.getText().equals("")) {
            ativaView();
            ativaSalvarCancelar();
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um Registro", "Atenção", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jBalterarActionPerformed

    private void jBexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBexcluirActionPerformed
        // TODO add your handling code here:
        if (!jTidusuario.getText().equals("")) {
            if (JOptionPane.showConfirmDialog(null, "Deseja Excluir o Registro", "Atenção", JOptionPane.YES_NO_OPTION) == 0) {
                try {
                    new UsuarioDao().deleteUsuario(Integer.valueOf(jTidusuario.getText()));
                    limpaView();
                    desativaView();
                    ativaNovoAlterarExcluir();
                    buscaUsuario();
                    JOptionPane.showMessageDialog(this,"Registro Excluido","Atenção", JOptionPane.INFORMATION_MESSAGE);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this,"Erro ao Excluir Registro\n"+ex,"Atenção",JOptionPane.ERROR_MESSAGE);
                }
            }
        }else {
            JOptionPane.showMessageDialog(this, "Selecione um Registro", "Atenção", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jBexcluirActionPerformed


            ub.setSsl(jCssl.isSelected());
            ub.setTls(jCtls.isSelected());
            ub.setEmail(jTemail.getText());
            ub.setSenhaemail(jPsenhaemail.getText());
            ub.setAssinatura(jTassinatura.getText());
            ub.setBconsulta(jCbConsulta.isSelected());

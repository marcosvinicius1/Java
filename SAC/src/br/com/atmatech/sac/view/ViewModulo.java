private void ativaView() {
        jTemail.setEditable(true);
        jTmodulo.setEditable(true);
        jTresponsavel.setEditable(true);
        jCativo.setEnabled(true);
        jTtmodulo.setEnabled(false);
        jTtmodulo.clearSelection();
    }

    private void desativaView() {
        jTemail.setEditable(false);
        jTmodulo.setEditable(false);
        jTresponsavel.setEditable(false);
        jCativo.setEnabled(false);
        jTtmodulo.setEnabled(true);
    }

    private void inicializaTabela() {
        jTtmodulo.getColumnModel().getColumn(0).setMaxWidth(0);
        jTtmodulo.getColumnModel().getColumn(0).setMinWidth(0);
        jTtmodulo.getColumnModel().getColumn(0).setPreferredWidth(0);
    }

    private void buscaModulos() {
        DefaultTableModel tabelamodulo = (DefaultTableModel) jTtmodulo.getModel();
        tabelamodulo.setNumRows(0);
        try {
            lmb = new ModuloDao().getModulo();
            for (ModuloBeans lmd1 : lmb) {
                tabelamodulo.addRow(new Object[]{lmd1.getIdmodulo(), lmd1.getDescricao(), lmd1.getResponsavel(), lmd1.getEmail()});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Consultar Modulos\n" + ex);
        }

    }

    private void buscaModulos(String campo, String parametro) {
        DefaultTableModel tabelamodulo = (DefaultTableModel) jTtmodulo.getModel();
        tabelamodulo.setNumRows(0);
        lmb = new ModuloDao().getModulo(campo, parametro);
        for (ModuloBeans lmd1 : lmb) {
            tabelamodulo.addRow(new Object[]{lmd1.getIdmodulo(), lmd1.getDescricao(), lmd1.getResponsavel(), lmd1.getEmail()});
        }
        if (tabelamodulo.getRowCount() == 0) {
            tabelamodulo.addRow(new Object[]{});

        }
    }

    private void clickModulo() {
        for (int i = 0; i < lmb.size(); i++) {
            if (i == jTtmodulo.getSelectedRow()) {
                jTemail.setText(lmb.get(i).getEmail());
                jTmodulo.setText(lmb.get(i).getDescricao());
                jTresponsavel.setText(lmb.get(i).getResponsavel());
                jCativo.setSelected(lmb.get(i).getAtivo());
                jTidmodulo.setText(String.valueOf(lmb.get(i).getIdmodulo()));
            }
        }
    }

    private void limpaTela() {
        jTemail.setText("");
        jTmodulo.setText("");
        jTresponsavel.setText("");
        jCativo.setSelected(true);
        jTidmodulo.setText("");
    }

    private void ativaNovoAlterarExcluir() {
        jBalterar.setEnabled(true);
        jBcancelar.setEnabled(false);
        jBexcluir.setEnabled(true);
        jBnovo.setEnabled(true);
        jBsalvar.setEnabled(false);
    }

    private void ativaSalvarCancelar() {
        jBalterar.setEnabled(false);
        jBcancelar.setEnabled(true);
        jBexcluir.setEnabled(false);
        jBnovo.setEnabled(false);
        jBsalvar.setEnabled(true);
    }

    private boolean validaView() {
        if (jTemail.getText().equals("")) {
            return false;
        }
        if (jTmodulo.getText().equals("")) {
            return false;
        }
        if (jTresponsavel.getText().equals("")) {
            return false;
        }
        return true;
    }

    private void premissaoUsuario() {
        int ver = 0;
        if (!new NivelAcesso().getAcesso("ViewModulo", "cadastrar", false)) {
            jBnovo.setVisible(false);
            ver++;
        }
        if (!new NivelAcesso().getAcesso("ViewModulo", "alterar", false)) {
            jBalterar.setVisible(false);
            ver++;
        }
        if (!new NivelAcesso().getAcesso("ViewModulo", "excluir", false)) {
            jBexcluir.setVisible(false);
            ver++;
        }
        if (ver == 3) {
            jPanel1.setVisible(false);
        }
    }

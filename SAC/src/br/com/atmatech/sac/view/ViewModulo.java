    private void jBexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBexcluirActionPerformed
        // TODO add your handling code here:
        if (!jTidmodulo.getText().equals("")) {
            if (JOptionPane.showConfirmDialog(null, "Deseja Excluir o Registro", "Atenção", JOptionPane.YES_NO_OPTION) == 0) {
                try {
                    new ModuloDao().deleteModulo(Integer.valueOf(jTidmodulo.getText()));
                    ativaNovoAlterarExcluir();
                    desativaView();
                    limpaTela();
                    buscaModulos();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Erro ao Excluir\n" + ex, "Atenção", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Registro", "Atenção", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBexcluirActionPerformed

    private void jTtmoduloKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTtmoduloKeyPressed
        // TODO add your handling code here:    
        int coluna = jTtmodulo.getSelectedColumn();
        if (evt.getKeyCode() == KeyEvent.VK_F) {
            if (evt.isControlDown()) {
                String text = JOptionPane.showInputDialog(this, "Pesquisa: " + jTtmodulo.getColumnName(coluna), "PESQUISA", JOptionPane.WARNING_MESSAGE);
                if (text != null) {
                    //DefaultTableModel tabela = (DefaultTableModel) jTtmodulo.getModel();
                    //final TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(tabela);
                    // jTtmodulo.setRowSorter(sorter);
                    text = text.toUpperCase();
                    if (jTtmodulo.getColumnName(coluna).equals("MODULO")) {
                        buscaModulos("descricao", text);
                    }
                    if (jTtmodulo.getColumnName(coluna).equals("EMAIL")) {
                        buscaModulos("email", text);
                    }
                    if (jTtmodulo.getColumnName(coluna).equals("RESPONSÁVEL")) {
                        buscaModulos("responsavel", text);
                    }
                }

            }
        }

    }//GEN-LAST:event_jTtmoduloKeyPressed



    private void jBsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBsalvarActionPerformed
        // TODO add your handling code here:
        if (validaView()) {
            ModuloBeans mb = new ModuloBeans();
            mb.setAtivo(jCativo.isSelected());
            mb.setDescricao(jTmodulo.getText());
            mb.setEmail(jTemail.getText());
            mb.setResponsavel(jTresponsavel.getText());
            mb.setIdmodulo(jTidmodulo.getText().equals("") ? 0 : Integer.valueOf(jTidmodulo.getText()));
            try {
                if (jTidmodulo.getText().equals("")) {
                    new ModuloDao().setModulo(mb);
                } else {
                    new ModuloDao().updateModulo(mb);
                }
                ativaNovoAlterarExcluir();
                desativaView();
                limpaTela();
                buscaModulos();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro\n" + ex, "Atenção", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Verifique as Informações Digitadas", "Atenção", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBsalvarActionPerformed


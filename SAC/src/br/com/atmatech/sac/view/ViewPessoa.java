    private void exportarArquivo(byte[] bytes, String nome, File local) throws FileNotFoundException, IOException {
        File f = new File(local + "\\" + nome);
        FileOutputStream fos = new FileOutputStream(f);
        fos.write(bytes);
        fos.close();
    }


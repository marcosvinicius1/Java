package br.com.atmatech.sac.beans;

/**
 *
 * @author marcos
 */
public class VersaoBeans {
    private Integer idversao;
    private Integer versao;
    private Integer edicao;
    private Integer seguranca;

    public Integer getIdversao() {
        return idversao;
    }

    public void setIdversao(Integer idversao) {
        this.idversao = idversao;
    }

    public Integer getVersao() {
        return versao;
    }

    public void setVersao(Integer versao) {
        this.versao = versao;
    }

    public Integer getEdicao() {
        return edicao;
    }

    public void setEdicao(Integer edicao) {
        this.edicao = edicao;
    }

    public Integer getSeguranca() {
        return seguranca;
    }

    public void setSeguranca(Integer seguranca) {
        this.seguranca = seguranca;
    }
    
    
}

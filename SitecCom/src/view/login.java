public void validalogin(){
    TelaPrincial tela = new TelaPrincial();//objeto da tela principal
        DadosEmpresaBins de= new DadosEmpresaBins();//objeto dados empresa
        tela.jLtelaprincipalempresa.setText(de.getNome().toUpperCase());//set nome da empresa na tela principal
         try {
            PessoaDao pd= new PessoaDao();
            lpessoa= pd.getLogin(jTnome.getText(), de.getId());
            if(lpessoa.size()>0){
            if( jPfsenha.getText().equals(lpessoa.get(0).getSenha())){
                tela.setVisible(true);//tela principal aberta
                PessoaUsuario pu=new PessoaUsuario();
                pu.setId(lpessoa.get(0).getId());
                pu.setNome(lpessoa.get(0).getNome());
                pu.setApelido(lpessoa.get(0).getApelido());
                pu.setSenha(lpessoa.get(0).getSenha());
                pu.setLembra(lpessoa.get(0).getLembra());
                pu.setAtivo(lpessoa.get(0).getAtivo());
                pu.setEmpresa(lpessoa.get(0).getEmpresa());
                tela.jLusuario.setText(pu.getApelido().toUpperCase());
                carregaAcesso(lpessoa.get(0).getId());//chama metodo que carrega permissoes do usuario
               login.this.dispose();//fecha tela login           
            }else{
                JOptionPane.showMessageDialog(null, "Senha nao confere");
            }
            }else{
                JOptionPane.showMessageDialog(null,"Usuario nao Encontrado");
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro"+ex);
        }
}
public void carregaAcesso(int id){//metodo carrega permissoes do usuario
    try {
        AcessoDao ad=new AcessoDao();//objeto classe acesso
        PermissoesUsuario pu=new PermissoesUsuario();//objeto classe permissoes
        lacesso=ad.getAcesso(id);//retorna pesquisa no banco de permissao de acesso do usuario
        for(int i=0;i<lacesso.size();i++){//preenche a classe permissoesusuario com os valores da lista lacesso
            if("CADASTRO PESSOAS".equals(lacesso.get(i).getClasse())){
                pu.setCadastropessoas(lacesso.get(i).getAcesso());
            }
            if("RECEBER".equals(lacesso.get(i).getClasse())){
                pu.setReceber(lacesso.get(i).getAcesso());
            }
            if("CADASTRO PRODUTO".equals(lacesso.get(i).getClasse())){
                pu.setCadastroproduto(lacesso.get(i).getAcesso());
            }
            if("ORCAMENTO".equals(lacesso.get(i).getClasse())){
                pu.setOrcamento(lacesso.get(i).getAcesso());
            }
            if("CAIXA".equals(lacesso.get(i).getClasse())){
                pu.setCaixa(lacesso.get(i).getAcesso());
            }
            if("EMPRESA".equals(lacesso.get(i).getClasse())){
                pu.setEmpresa(lacesso.get(i).getAcesso());
            }
            if("BACKUP".equals(lacesso.get(i).getClasse())){
                pu.setBackup(lacesso.get(i).getAcesso());
            }
            if("PERMISSAO".equals(lacesso.get(i).getClasse())){
                pu.setPermissao(lacesso.get(i).getAcesso());
            }
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Erro ao Registrar Permissoes\n"+ex);
    }
    
}

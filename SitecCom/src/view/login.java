public void tabelaempresa(List<EmpresaBins> eb){
    DefaultTableModel modeloempresa= (DefaultTableModel) jTdempresa.getModel();
    modeloempresa.setNumRows(0);
    for (int i=0;i<eb.size();i++){
        modeloempresa.addRow(new Object[]{eb.get(i).getId(),eb.get(i).getNome()});
    }
}

    public float custoliquido(){
    float precocusto=Float.parseFloat(jFprecocusto.getText().replace(",", "."));
    float frete=Float.parseFloat(jFfrete.getText().replace(",", "."));
    float acessorio=Float.parseFloat(jFacessorio.getText().replace(",", "."));
    float ipi=Float.parseFloat(jFipi.getText().replace(",", "."));
    //float valortrib=Float.parseFloat(jFvalortributado.getText().replace(",","."));
    float custoliquido=((precocusto*ipi/100)+precocusto+acessorio+frete/*+valortrib*/);
    return custoliquido;
    }
    
    public float precovenda(){
    float margem=Float.parseFloat(jFmargemlucro.getText().replace(",", "."));
    float custoreal=Float.parseFloat(jFcustoreal.getText().replace(",", "."));
   // float valortrib=Float.parseFloat(jFvalortributado.getText().replace(",", "."));
    float precovenda=((custoreal*margem/100)+custoreal);
    return precovenda;
}
    
    public float calcaliquota(){
    float aliquota=Float.parseFloat(jFaliquota.getText().replace(",", "."));
    float precovenda=Float.parseFloat(jFprecovenda.getText().replace(",", "."));
    float valortributado=(precovenda*aliquota/100);
    return valortributado;
}
   

    public void alteraproduto() {
        try {
            ProdutoBins pb = new ProdutoBins();
            pb.setAliquota(Integer.valueOf(jTdidaliquota.getText()));
            pb.setGrupo(Integer.valueOf(jTdidgrupo.getText()));
            pb.setMarca(Integer.valueOf(jTdidmarca.getText()));
            pb.setPessoa(Integer.valueOf(jTdidpessoa.getText()));
            pb.setCodbarra(jTcodbarra.getText());
            pb.setDescricao(jTdescricao.getText());
            pb.setQtde(Float.valueOf(jFqtde.getText().replace(",", ".")));
            pb.setQtdemin(Float.valueOf(jFqtdemin.getText().replace(",", ".")));
            pb.setLocalizacao(jTlocalizacao.getText());
            pb.setMargemlucro(Float.valueOf(jFmargemlucro.getText().replace(",", ".")));
            pb.setPrecocusto(Float.valueOf(jFprecocusto.getText().replace(",", ".")));
            pb.setPrecovenda(Float.valueOf(jFprecovenda.getText().replace(",", ".")));
            pb.setFrete(Float.valueOf(jFfrete.getText().replace(",", ".")));
            pb.setAcessorio(Float.valueOf(jFacessorio.getText().replace(",", ".")));
            pb.setAtivo(selecaoativo());
            pb.setValoraliq(Float.valueOf(jFvalortributado.getText().replace(",", ".")));
            pb.setCustoreal(Float.valueOf(jFcustoreal.getText().replace(",", ".")));
            pb.setId(Integer.valueOf(jTid.getText()));

            ProdutoDao pd = new ProdutoDao();
            pd.altera(pb);
            JOptionPane.showMessageDialog(null, "Produto Alterado");
        } catch (SQLException | NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Alterar produto\n" + ex);
        }
    }
    

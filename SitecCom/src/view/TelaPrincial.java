public void telaempresa(){
     if (!empresa.isVisible()) {
            SenhaAdm adm = new SenhaAdm();
            adm.jDsenhaEmpresa.setLocationRelativeTo(null);
            adm.jPasswordFieldsenha2.setText("");
            adm.jDsenhaEmpresa.setVisible(true);
            String senha = adm.jPasswordFieldsenha2.getText();
            DadosEmpresaBins de = new DadosEmpresaBins();
            if (senha.equals(de.getSenha())) {
                empresa.setVisible(true);
                empresa.carregaEmpresa();
                empresa.desativa();
            } else {
                JOptionPane.showMessageDialog(null, "Senha Incorreta");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Janela Aberta");
        }
}

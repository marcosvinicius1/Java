    public void threadBackup() {
        new Thread(new Runnable() {
            @Override
            public void run() {
                Backup backup = new Backup();
                jLbackup.setText("BACKUP SENDO PROCESSADO");
                if (backup.backupSistema()) {
                    jLbackup.setText("");
                    JOptionPane.showMessageDialog(null, "Backup Efetuado com Sucesso");
                } else {
                    JOptionPane.showMessageDialog(null, "Falha na Execulsão do Backup");
                }
            }
        }).start();
    }
